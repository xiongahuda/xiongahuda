function getbilltable(object){
	
	var str = '<table id="billtable" border="5" style="font-weight:normal;bordercolor:#CD853F;font-size:20px;width:500px;height:360px;bgcolor:#CD853F;"><th>备注</th><th>类别</th><th>金额</th><th>时间</th><th>del</th>';
