		if(v.pay==1){
			var pay = '收入';
		}else{
			var pay = '支出';
		}
		str+='<tr style="border: solid 1px #CD853F;">';
		str+='<td align="center">'+v.notes+'</td>';
		str+='<td align="center">'+pay+'</td>';
		str+='<td align="center">'+v.money+'￥</td>';
